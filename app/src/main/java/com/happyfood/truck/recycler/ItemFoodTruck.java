public class ItemFoodTruck {
    String namefood, info, instaweb, img, kindfood, event;
    float rating;

    public void setNamefood(String namefood) {
        this.namefood = namefood;
    }

    public String getNamefood() {
        return namefood;
    }

    public void setRating(float rating) {
        this.rating = rating;
    }

    public float getRating() {
        return rating;
    }

    public String setInfo(String info) {
        return this.info = info;
    }
    public String getInfo() {
        return info;
    }

    public void setInstaweb(String instaweb) {
        this.instaweb = instaweb;
    }

    public String getInstaweb() {
        return instaweb;
    }

    public void setImg (String img) {
        this.img = img;
    }

    public String getImg() {
        return img;
    }

    public void setKindfood (String kindfood) {
        this.img = kindfood;
    }

    public String getKindfood() {
        return kindfood;
    }

    public void setEvent (String event) {
        this.img = event;
    }

    public String getEvent() {
        return event;
    }


}


